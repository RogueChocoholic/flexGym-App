    private void addBrand() {
        String brandName = jTextField1.getText();
        if (brandName.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, 3000l, "Please enter a brand name");
        } else {
            brandName = brandName.trim();
            try {
                ResultSet brandSet = MySQL.executeSearch("SELECT * FROM `brand` WHERE  `brand_name` ='" + brandName + "'  ");

                if (brandSet.next()) {
                    Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, 3000l, "Brand already exists in the system");
                } else {
                    MySQL.executeIUD("INSERT INTO `brand` (`brand_name`) VALUES ('" + brandName + "')");
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, 3000l, "Brand Added Successsfully");

                }

            } catch (Exception e) {
                e.printStackTrace();
                SplashScreen.exceptionRecords.log(Level.SEVERE, "Unable to add Brand");
            }

        }
    }

    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            ResultSet brandSet = MySQL.executeSearch("SELECT * FROM `brand` ");
            while (brandSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(brandSet.getString("brand_id"));
                vector.add(brandSet.getString("brand_name"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
            SplashScreen.exceptionRecords.log(Level.SEVERE, "Unable to load table at adding Brand");
        }
    }

