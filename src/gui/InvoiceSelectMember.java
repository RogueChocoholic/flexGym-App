    private void loadMembers() {

        String memID = jTextField1.getText();
        String search = " WHERE `mem_id` LIKE '%" + memID + "%'";

        String name = jTextField2.getText();
        search += " AND (`fname` LIKE '%" + name + "%' OR `lname` LIKE '%" + name + "%')";

        String mobile = jTextField3.getText();
        search += " AND `mobile` LIKE '%" + mobile + "%'";
        try {
            ResultSet memberSet = MySQL.executeSearch("SELECT * FROM `member` INNER JOIN"
                    + " `membership_records` ON `member`.`mem_id` = `membership_records`.`member_mem_id` " + search);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (memberSet.next()) {
                Vector<String> memberData = new Vector<>();
                memberData.add(memberSet.getString("mem_id"));
                memberData.add(memberSet.getString("fname") + " " + memberSet.getString("lname"));
                memberData.add(memberSet.getString("mobile"));
                memberData.add(memberSet.getString("email"));

                memberData.add(validateMembershipStatus(memberSet.getString("expire_date")));
                model.addRow(memberData);
            }

        } catch (Exception e) {
            e.printStackTrace();
            SplashScreen.exceptionRecords.log(Level.WARNING, "Unable to create barcode", e);
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, 3000l, "Couldn't load products. Please check your internet connection.");
        }
    }

    private String validateMembershipStatus(String exp) {
        LocalDate expDate = LocalDate.parse(exp);
        LocalDate today = LocalDate.now();
        if (expDate.isAfter(today)) {
            return "Active";
        } else {
            return "Expired";
        }
    }

    private void setMemberData() {
        if (jTable1.getSelectedRowCount() == 1) {
            int row = jTable1.getSelectedRow();

            InvoiceFrame.setMemberDetails().get(0).setText(String.valueOf(jTable1.getValueAt(row, 0)));
            InvoiceFrame.setMemberDetails().get(1).setText(String.valueOf(jTable1.getValueAt(row, 1)));
            InvoiceFrame.setMemberDetails().get(2).setText(String.valueOf(jTable1.getValueAt(row, 3)));
            InvoiceFrame.setMemberDetails().get(3).setText(String.valueOf(jTable1.getValueAt(row, 4)));
            this.dispose();
        }
    }

