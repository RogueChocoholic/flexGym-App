    private void loginAction() {
        String email = jTextField1.getText();
        String pw = String.valueOf(jPasswordField1.getPassword());
        
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `staff` INNER JOIN `employee_type` ON"
                    + " `staff`.`employee_type_type_id` = `employee_type`.`type_id` WHERE `email` = '" + email + "' "
                    + " AND `password` = '" + pw + "' ");
            
            if (resultSet.next()) {
                setEmployeeName(resultSet.getString("fname") + " " + resultSet.getString("lname"));
                setEmployeeEmail(resultSet.getString("email"));
                setEmplyeeID(resultSet.getString("staff_id"));
                setEmployeeType(resultSet.getString("type_name"));
                
                Date date = new Date();
                setLoginDate(FormatDate.getDate(date, "yyyy-MM-dd"));
                setLoginTime(FormatDate.getDate(date, "HH:mm"));
                
                Home home = new Home(true);
                this.dispose();
                home.setVisible(true);
                SplashScreen.loginRecords.severe("New Login :" + getEmplyeeID() + " : " + getEmployeeName() + " : at " + getloginTime() + " : " + getloginDate());
            } else {
                JOptionPane.showMessageDialog(this, "Email or Password Invalid", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
